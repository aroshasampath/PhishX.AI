function renderReport(data) {
    const report = document.getElementById("report");
    report.classList.remove("hidden");

    const level = data.risk_level || "Unknown";
    const score = data.risk_score || 0;
    const verdict = data.verdict || "Unknown";
    const levelClass = level.toLowerCase();

    let simpleMessage = "This email looks mostly safe.";
    let advice = "You can continue, but always be careful with unexpected emails.";
    let icon = "✅";

    if (level === "High") {
        simpleMessage = "This email looks dangerous and may be a phishing attempt.";
        advice = "Do not click links, do not download attachments, and do not enter passwords, OTPs, PINs or card details.";
        icon = "🚨";
    } else if (level === "Medium") {
        simpleMessage = "This email has suspicious signs. Be careful before trusting it.";
        advice = "Check the sender carefully, avoid clicking links, and verify using the official website.";
        icon = "⚠️";
    }

    report.innerHTML = `
        <div class="report-header">
            <div>
                <h2>${icon} Scan Report</h2>
                <p>Simple security summary generated by PhishX.AI</p>
            </div>
            <span class="badge ${levelClass}">${level} Risk</span>
        </div>

        <div class="score-card">
            <div class="score ${levelClass}">
                ${score}
                <small>/100</small>
            </div>
            <div style="flex:1">
                <h3>${verdict}</h3>
                <p>${simpleMessage}</p>
                <div class="risk-meter">
                    <div class="risk-meter-fill ${levelClass}" style="width:${score}%"></div>
                </div>
            </div>
        </div>

        <div class="simple-section">
            <h3>What this means</h3>
            <p>${simpleMessage}</p>
        </div>

        <div class="simple-section">
            <h3>Detected Issues</h3>
            <ul>
                ${(data.reasons && data.reasons.length > 0
                    ? data.reasons.map(r => `<li>${makeReasonSimple(r)}</li>`).join("")
                    : "<li>No major suspicious issue found.</li>"
                )}
            </ul>
        </div>

        <div class="simple-section advice">
            <h3>Recommended Action</h3>
            <p>${advice}</p>
        </div>

        <details class="technical-details">
            <summary>Show Technical Details</summary>

            <div class="simple-grid">
                <div>
                    <h3>Email Info</h3>
                    <pre>${JSON.stringify(data.email_info || {}, null, 2)}</pre>
                </div>

                <div>
                    <h3>URLs Found</h3>
                    <pre>${JSON.stringify(data.report?.urls_found || [], null, 2)}</pre>
                </div>

                <div>
                    <h3>Suspicious URLs</h3>
                    <pre>${JSON.stringify(data.report?.suspicious_urls || [], null, 2)}</pre>
                </div>

                <div>
                    <h3>Authentication</h3>
                    <pre>${JSON.stringify(data.report?.email_authentication || {}, null, 2)}</pre>
                </div>

                <div>
                    <h3>Attachments</h3>
                    <pre>${JSON.stringify({
                        attachments: data.report?.attachments || [],
                        suspicious_attachments: data.report?.suspicious_attachments || []
                    }, null, 2)}</pre>
                </div>

                <div>
                    <h3>Domain Check</h3>
                    <pre>${JSON.stringify(data.report?.domain_check || {}, null, 2)}</pre>
                </div>
            </div>
        </details>
    `;

    report.scrollIntoView({ behavior: "smooth" });
}

function makeReasonSimple(reason) {
    const map = {
        "Suspicious words/phrases detected": "The email uses urgent or suspicious language.",
        "Password / OTP / PIN / card details request detected": "The email asks for sensitive information such as password, OTP, PIN or card details.",
        "Suspicious URL rules matched": "The email contains suspicious links.",
        "HTML visible link vs actual link mismatch detected": "The visible link and the real link are different.",
        "SPF failed": "The sender email authentication failed.",
        "DKIM failed": "The email signature verification failed.",
        "DMARC failed": "The domain protection check failed.",
        "Suspicious attachment detected": "The email has a suspicious attachment.",
        "DNS resolve failed": "The sender domain could not be verified.",
        "MX record not found": "The sender domain has no valid mail record.",
        "Domain is very new": "The sender domain is newly created.",
        "No sender domain found": "Sender domain was not provided.",
        "OCR text extracted from image": "Text was extracted from the uploaded image.",
        "Password request found in image text": "The image contains a password request.",
        "OTP request found in image text": "The image contains an OTP request.",
        "Verification request found in image text": "The image asks the user to verify something.",
        "Login request found in image text": "The image contains a login request."
    };

    return map[reason] || reason;
}
